// Helper function to get temperature data based on location
export function getTemperatureData(country, region = null) {
  if (!country || !temperatureDataByLocation[country]) {
    return temperatureData; // Return default
  }
  
  if (region && temperatureDataByLocation[country][region]) {
    return temperatureDataByLocation[country][region];
  }
  
  // Return first available region for the country
  const regions = Object.keys(temperatureDataByLocation[country]);
  return temperatureDataByLocation[country][regions[0]];
}

// Helper function to get soil moisture data based on location
export function getSoilMoistureData(country, region = null) {
  if (!country || !soilMoistureDataByLocation[country]) {
    return soilMoistureData; // Return default
  }
  
  if (region && soilMoistureDataByLocation[country][region]) {
    return soilMoistureDataByLocation[country][region];
  }
  
  // Return first available region for the country
  const regions = Object.keys(soilMoistureDataByLocation[country]);
  return soilMoistureDataByLocation[country][regions[0]];
}

